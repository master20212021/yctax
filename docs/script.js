if (contactForm) {
  contactForm.addEventListener("submit", (event) => {
    event.preventDefault();

    const dictionary = translations[currentLanguage];
    const formData = new FormData(contactForm);
    const serviceLabel = contactForm.querySelector('select[name="service"] option:checked')?.textContent || "";
    const submitButton = contactForm.querySelector('button[type="submit"]');
    const fallbackMailto = buildMailtoLink(dictionary, formData, serviceLabel);

    contactForm.classList.add("is-submitting");
    if (submitButton) {
      submitButton.textContent = dictionary.formButtonSending;
    }
    setFormStatus("");

    fetch(formSubmitEndpoint, {
      method: "POST",
      headers: {
        "Content-Type": "application/json",
        Accept: "application/json",
      },
      body: JSON.stringify({
        name: formData.get("name"),
        email: formData.get("email"),
        service: serviceLabel,
        message: formData.get("message"),
        language: currentLanguage,
        _subject: `${dictionary.mailSubjectPrefix} - ${serviceLabel}`,
        _captcha: "false",
      }),
    })
      .then((response) => {
        if (!response.ok) {
          throw new Error("Form request failed");
        }
        return response.json();
      })
      .then((result) => {
        if (String(result?.success).toLowerCase() !== "true") {
          throw new Error(result?.message || "Form backend rejected submission");
        }

        contactForm.reset();
        setFormStatus(dictionary.formStatusSuccess, "is-success");
      })
      .catch(() => {
        setFormStatus(dictionary.formStatusError, "is-error");
        window.location.href = fallbackMailto;
      })
      .finally(() => {
        contactForm.classList.remove("is-submitting");
        if (submitButton) {
          submitButton.textContent = dictionary.formButton;
        }
      });
  });
}

function setupMobileSectionDock() {
  const mobileQuery = window.matchMedia("(max-width: 760px)");
  const dock = document.querySelector(".mobile-section-dock");

  if (!dock) {
    return;
  }

  const links = Array.from(dock.querySelectorAll("a[href^='#']"));
  const targets = links
    .map((link) => {
      const target = document.querySelector(link.getAttribute("href"));
      return target ? { link, target } : null;
    })
    .filter(Boolean);

  if (!targets.length) {
    return;
  }

  const setActiveLink = (nextLink) => {
    links.forEach((link) => {
      link.classList.toggle("is-active", link === nextLink);
    });

    nextLink.scrollIntoView({ behavior: "smooth", block: "nearest", inline: "center" });
  };

  let currentActiveLink = links[0];
  setActiveLink(currentActiveLink);

  const observer = new IntersectionObserver(
    (entries) => {
      if (!mobileQuery.matches) {
        return;
      }

      const visibleEntries = entries
        .filter((entry) => entry.isIntersecting)
        .sort((entryA, entryB) => entryB.intersectionRatio - entryA.intersectionRatio);

      if (!visibleEntries.length) {
        return;
      }

      const matched = targets.find(({ target }) => target === visibleEntries[0].target);
      if (!matched || matched.link === currentActiveLink) {
        return;
      }

      currentActiveLink = matched.link;
      setActiveLink(currentActiveLink);
    },
    {
      threshold: [0.25, 0.45, 0.65],
      rootMargin: "-12% 0px -48% 0px",
    }
  );

  targets.forEach(({ target }) => observer.observe(target));

  links.forEach((link) => {
    link.addEventListener("click", () => {
      currentActiveLink = link;
      setActiveLink(link);
    });
  });
}

