const menuToggle = document.querySelector(".menu-toggle");
const siteNav = document.querySelector(".site-nav");
const faqItems = document.querySelectorAll(".faq-item");
const revealItems = document.querySelectorAll(".reveal-on-scroll");
const languageButtons = document.querySelectorAll(".language-button");
const metaDescription = document.querySelector('meta[name="description"]');
const canonical = document.querySelector('link[rel="canonical"]');
const ogTitle = document.querySelector('meta[property="og:title"]');
const ogDescription = document.querySelector('meta[property="og:description"]');
const twitterTitle = document.querySelector('meta[name="twitter:title"]');
const twitterDescription = document.querySelector('meta[name="twitter:description"]');
const whatsappLinks = document.querySelectorAll(".whatsapp-link");
const whatsappNumber = "19293203899";
const pageKey = document.body.dataset.servicePage;

function detectLanguage() {
  const savedLanguage = localStorage.getItem("yc-tax-language");
  if (savedLanguage && globalTranslations[savedLanguage]) {
    return savedLanguage;
  }

  const browserLanguages = navigator.languages || [navigator.language || "en"];
  const hasSpanish = browserLanguages.some((language) => language.toLowerCase().startsWith("es"));
  return hasSpanish ? "es" : "en";
}

let currentLanguage = detectLanguage();

function updateWhatsAppLinks(language) {
  const message = encodeURIComponent(globalTranslations[language].whatsappMessage);
  const href = `https://wa.me/${whatsappNumber}?text=${message}`;
  whatsappLinks.forEach((link) => link.setAttribute("href", href));
}

function applyLanguage(language, persist = true) {
  const globalDictionary = globalTranslations[language];
  const pageDictionary = pageContent[pageKey][language];

  document.documentElement.lang = language;
  document.title = pageDictionary.pageTitle;
  if (metaDescription) {
    metaDescription.setAttribute("content", pageDictionary.metaDescription);
  }
  if (canonical) {
    canonical.setAttribute("href", `https://yctaxservice.com/${pageContent[pageKey].path}`);
  }
  if (ogTitle) {
    ogTitle.setAttribute("content", pageDictionary.pageTitle);
  }
  if (ogDescription) {
    ogDescription.setAttribute("content", pageDictionary.metaDescription);
  }
  if (twitterTitle) {
    twitterTitle.setAttribute("content", pageDictionary.pageTitle);
  }
  if (twitterDescription) {
    twitterDescription.setAttribute("content", pageDictionary.metaDescription);
  }

  document.querySelectorAll("[data-global]").forEach((element) => {
    const key = element.dataset.global;
    if (globalDictionary[key] !== undefined) {
      element.textContent = globalDictionary[key];
    }
  });

  document.querySelectorAll("[data-page]").forEach((element) => {
    const key = element.dataset.page;
    if (pageDictionary[key] !== undefined) {
      element.textContent = pageDictionary[key];
    }
  });

  languageButtons.forEach((button) => {
    button.classList.toggle("is-active", button.dataset.lang === language);
  });

  updateWhatsAppLinks(language);
  currentLanguage = language;
  if (persist) {
    localStorage.setItem("yc-tax-language", language);
  }
}

languageButtons.forEach((button) => {
  button.addEventListener("click", () => applyLanguage(button.dataset.lang));
});

document.querySelectorAll("img[data-fallback-src]").forEach((image) => {
  image.addEventListener("error", () => {
    const fallbackSource = image.dataset.fallbackSrc;
    if (fallbackSource && image.getAttribute("src") !== fallbackSource) {
      image.setAttribute("src", fallbackSource);
    }
  });
});

if (menuToggle && siteNav) {
  menuToggle.addEventListener("click", () => {
    const expanded = menuToggle.getAttribute("aria-expanded") === "true";
    menuToggle.setAttribute("aria-expanded", String(!expanded));
    siteNav.classList.toggle("is-open");
  });
}

faqItems.forEach((item) => {
  const button = item.querySelector(".faq-question");
  if (!button) {
    return;
  }

  button.addEventListener("click", () => {
    const isOpen = item.classList.contains("is-open");
    faqItems.forEach((entry) => {
      entry.classList.remove("is-open");
      const entryButton = entry.querySelector(".faq-question");
      if (entryButton) {
        entryButton.setAttribute("aria-expanded", "false");
      }
    });
    if (!isOpen) {
      item.classList.add("is-open");
      button.setAttribute("aria-expanded", "true");
    }
  });
});

const revealObserver = new IntersectionObserver(
  (entries) => {
    entries.forEach((entry) => {
      if (entry.isIntersecting) {
        entry.target.classList.add("is-visible");
        revealObserver.unobserve(entry.target);
      }
    });
  },
  { threshold: 0.16 }
);

revealItems.forEach((item) => {
  if (!item.classList.contains("is-visible")) {
    revealObserver.observe(item);
  }
});

applyLanguage(currentLanguage, false);